""" Tipos int e float """
# int --> Números inteiros sem parte decimal (positivos, negativos)
print(1, -2, 0)

# float --> Números com parte decimal - ponto flutuante (positivos, negativos)
print(1.5, -2.7, 0.3)

""" Função type """
# Mostra o tipo que o Python inferiu ao valor.
print(type('Otávio')) # <class 'str'>
print(type(0))        # <class 'int'>
print(type(1.1))      # <class 'float'>