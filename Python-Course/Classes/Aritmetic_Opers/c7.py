""" Adição """
print('Adição: ', 10 + 10)          # 20

""" Subtração """
print('Subtração: ', 5 - 8)         # -3

""" Multiplicação """
print('Multiplicação: ', 6 * 4)     # 24

""" Divisão """
print('Divisão: ', 15 / 4)          # 3.75

''' Divisão Inteira '''
print('Divisão inteira: ', 15 // 4) # 3
 
''' Módulo (Resto da divisão) '''
print('Módulo: ', 55 % 2)           # 1

""" Exponenciação """
print('Exponenciação: ', 12 ** 2)   # 144

print(10 % 8 == 2) # True
print(16 % 8 == 1) # False
print(10 % 2 == 0) # True