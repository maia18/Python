""" Tipo de dado bool (boolean) """
# Há apenas duas respostas possíveis para quaisquer questionamentos: sim (True) ou não (False).
# Existem vários operadores para "questionar".
# '==' é um operador lógico que questiona se um valor é igual a outro

print(10 == 10)  # Sim => True
print(10 == 11)  # Não => False
print(type(True))       # <class 'bool'>
print(type(10 == 10))   # <class 'bool'>
print(type(10 == 11))   # <class 'bool'>