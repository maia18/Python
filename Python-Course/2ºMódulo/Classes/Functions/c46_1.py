""" map, partial, GeneratorType e esgotamento de Iterators """

''' map '''  
#    - Cria um iterador que aplica a função usando argumentos de cada um dos iteráveis. 
#    - Para mapear dados.
#    - Recebe dois argumentos principais: 
#           1) Uma função que define a transformação em cada item;
#           2) Um ou mais iteráveis os quais possuem os items passados à função.

''' partial ''' 
#     - Nova função com aplicação parcial dos argumentos e palavras-chave fornecidos. 
#     - Pode ser útil quando se tem funções que precisam ser chamadas múltiplas vezes com alguns dos argumentos dados.
#     - Ao invés de passar tais argumentos toda vez, pode-se criar uma nova função com estes já setados previamente.

''' GeneratorType ''' 
#     - Você pode usá-lo para verificar se um objeto é um gerador. 
#     - Pode ser útil para type checking ou debugging purposes.